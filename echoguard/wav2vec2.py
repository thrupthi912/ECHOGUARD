for idx,row in tqdm(
    df.iterrows(),
    total=len(df)
):

    audio, sr = librosa.load(
        row.path,
        sr=16000
    )

    inputs = feature_extractor(
        audio,
        sampling_rate=16000,
        return_tensors="pt"
    )

    input_values = inputs.input_values.to(
        device
    )

    with torch.no_grad():

        wav_out = model.wav2vec2(
            input_values
        )

        embedding = (
            wav_out
            .last_hidden_state
            .mean(dim=1)
            .squeeze()
            .cpu()
            .numpy()
        )

        logits = model(
            input_values=input_values
        ).logits

        probs = softmax(
            logits
        ).squeeze()

    probs_np = probs.cpu().numpy()

    confidence = probs_np.max()

    fearful = probs_np[5]

    angry = probs_np[4]

    sad = probs_np[3]

    stress_score = (
        fearful
        + 0.8*angry
        + 0.4*sad
    )

    feature = {

        "path":
            row.path,

        "label":
            row.label,

        "stress_score":
            float(stress_score),

        "confidence":
            float(confidence)
    }

    for i,p in enumerate(
        probs_np
    ):

        feature[
            f"emotion_{i}"
        ] = float(p)

    for i,e in enumerate(
        embedding
    ):

        feature[
            f"emb_{i}"
        ] = float(e)

    features.append(
        feature
    )

