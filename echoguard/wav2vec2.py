class RAVDESSDataset(Dataset):

    def __init__(self, dataframe):

        self.df = dataframe.reset_index(drop=True)

    def __len__(self):
        return len(self.df)

    def __getitem__(self, idx):

        row = self.df.iloc[idx]

        audio, sr = librosa.load(
            row.path,
            sr=16000
        )

        inputs = feature_extractor(
            audio,
            sampling_rate=16000,
            return_tensors="pt"
        )

        return {
            "input_values":
                inputs.input_values.squeeze(0),

            "label":
                torch.tensor(
                    row.label,
                    dtype=torch.long
                ),

            "path":
                row.path
        }

def collate_fn(batch):

    audio = [
        x["input_values"]
        for x in batch
    ]

    labels = torch.stack(
        [x["label"] for x in batch]
    )

    paths = [
        x["path"] for x in batch
    ]

    padded = torch.nn.utils.rnn.pad_sequence(
        audio,
        batch_first=True
    )

    return {
        "input_values": padded,
        "labels": labels,
        "paths": paths
    }

